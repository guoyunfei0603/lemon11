class MathMenthod:

    def __init__(self, a, b):
        self.a = a
        self.b = b

    def add(self):# 加法
        return self.a + self.b

    def subtract(self):# 减法
        return self.a - self.b

    def mul(self):# 乘法
        return self.a * self.b

    def dev(self):
        return self.a / self.b



